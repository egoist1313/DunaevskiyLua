function clicker_craft()
    local ff = false
    for i = 1, 1000 do if string.lower(sampTextdrawGetString(i)):find("craft") then ff = true break end end

    if ff then
    	-- Проверка нету ли готового крафта
    	local obj = getObjectsOfTable(slot_craft)
    	if obj ~= nil and obj[1] ~= nil and obj[1].model ~= 19478 then
    		-- Крафт уже собран
    		if antiflood == nil or os.clock() * 1000 - antiflood > delay then
    			antiflood = os.clock() * 1000
    			sampSendClickTextdraw(obj[1].id)
    		end
    		return
    	end

    	-- Проверка что щас крафтится
    	local craft_models = 0 -- То что крафтится щас

    	local obj = getObjectsOfTable(slots_set)
    	for i = 1, #obj do
    		if obj[i] ~= nil and obj[i].model ~= 19478 then
    			-- Проверить к какому крафту относится объект
    			local result, craft_id = checkObjectToCraft(obj[i].model)
    			if result then
    				craft_models = craft_id
    				-- Понятно что крафтится
    			end
    		end
    	end

    	-- Если ничего не крафтится, проверить что можно скрафтить первый попавшийся предмет в том что дано
    	if craft_models == 0 then
    		local result, craft_mb = checkGiveSlots()
    		if result then
    			craft_models = craft_mb
    		end
    	end

    	-- Зная что можно скрафтить - проверка моделей стоят ли они в своих слотах, если не стоят поиск в give и установка
    	if craft_models ~= 0 then
    		local result, textdrawId_get, textdrawId_set = checkSlotToCraftModel(craft_models)
    		if result then
    			repeat wait(0) until (antiflood == nil or os.clock() * 1000 - antiflood > delay)
    			sampSendClickTextdraw(textdrawId_get)
    			antiflood = os.clock() * 1000
    			repeat wait(0) until (antiflood == nil or os.clock() * 1000 - antiflood > (delay < 200 and 200 or delay))
    			sampSendClickTextdraw(textdrawId_set)
    			antiflood = os.clock() * 1000
    			-- ждать пока появится результат или прошло больше секунды
    			repeat 
    				wait(0) 
					if sampTextdrawIsExists(textdrawId_set) then
						local model = select(1, sampTextdrawGetModelRotationZoomVehColor(textdrawId_set))
						if model ~= 19478 then
							break
						end
					end
    			until (antiflood == nil or os.clock() * 1000 - antiflood > 1000)
    		end
    	end
    end
end
function getObjectsOfTable(table)
	local result = {}
	for i = 0, 2500 do
		if sampTextdrawIsExists(i) then
			local x, y = sampTextdrawGetPos(i)
			for k,v in pairs(table) do
				local dist = getDistanceBetweenCoords2d(x, y, v[1], v[2])
				if dist <= 0.3 then
					local model = select(1, sampTextdrawGetModelRotationZoomVehColor(i))
					result[k] = { model = model, id = i }
					break
				end
			end
		end
	end
	return result
end
function checkObjectToCraft(model)
	local result = false
	local craft_id = 0
	for k,v in pairs(craft_model) do
		for i,s in pairs(v.set) do
			if s == model then
				result = true
				craft_id = k
				break
			end
			if result then break end
		end 
		if result then break end
	end
	return result, craft_id
end
function checkGiveSlots()
	-- Проверить первый попавшийся модель на какой крафт совпадает
	local result = false
	local craft_model = 0
	local obj = getObjectsOfTable(slots_give) -- Получить объекты по координатам
	for i = 1, #obj do
		if obj[i] ~= nil and obj[i].model ~= 19478 then
			-- Проверить к какому крафту относится объект
			local res, craft_id = checkObjectToCraft(obj[i].model)
			if res then
				result = true
				craft_model = craft_id
				break
				-- Понятно что крафтить
			end
		end
	end
	return result, craft_model
end
function checkSlotToCraftModel(craft_models)
	local result = false
	local textdrawId_slot_set = 0
	local textdrawId_slot_get = 0
	if craft_model[craft_models] ~= nil then
		-- Получить номера слотов для данного крафта
		-- Найти первый свободный, заполнить его
		local free_slot = 0
		local position = craft_model[craft_models].position
		for i = 1, #position do
			local slot_number = position[i] -- Номер в массиве столов сет
			local slot_pos = slots_set[slot_number] -- Массив с позицией слота текущего
			local obj = getObjectsOfTable({slot_pos}) -- Узнать модель текстдрава
			if obj ~= nil and obj[1] ~= nil and obj[1].model == 19478 then -- Слот свободный
				free_slot = i
				textdrawId_slot_set = obj[1].id
				break
			end
		end
		if free_slot ~= 0 then -- Если нашелся свободный слот, найти для него объект из get
			-- Получить номер модели которая сюда нужна
			local model = craft_model[craft_models].set[free_slot]
	    	local obj = getObjectsOfTable(slots_give)
	    	for i = 1, #obj do
	    		if obj[i] ~= nil and obj[i].model == model then
	    			result = true
	    			textdrawId_slot_get = obj[i].id
	    			break
	    			-- Получил ид ячейски с нужным текстдравом
	    		end
	    	end
		end
	end
	return result, textdrawId_slot_get, textdrawId_slot_set
end
