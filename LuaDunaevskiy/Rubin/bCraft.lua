function route_rusn()
	if route_run ~= nil and #route_run > 0 then
    	repeat
    		wait(0)
    		local posX, posY, _ = getCharCoordinates(PLAYER_PED)
        	local x, y, jump, sprint = route_run[1][1], route_run[1][2], route_run[1][3], route_run[1][4]
        	local dist = getDistanceBetweenCoords2d(posX, posY, x, y)
        	if dist < 20 then
				setGameKeyState(1, -128)
				set_camera_pos_unfix(tonumber(x), tonumber(y))
				if sprint then
					setGameKeyState(16, 255)
					_sprint = os.time()
				elseif jump then
					setGameKeyState(16, 0)
					setGameKeyState(14, 255)
				end
        	else
        		route_run = {}
        		sampAddChatMessage("Чёт ты сбился с маршрута", -1)
        	end
        	if dist <= 1 then
        		if route_run[1] ~= nil then
        			table.remove(route_run, 1)
        		end
        	end
    	until dist > 20 or route_run[1] == nil or not activation
        route_run = {}
        if route_enter then
        	route_enter = false
        	stop_flood_enter = false
            lua_thread.create(function()
            	repeat
	                sendKey(16)
            		wait(1000)
	            until stop_flood_enter or not activation
            end)
        end
    end
end


