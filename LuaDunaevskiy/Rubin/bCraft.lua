function sampev.onServerMessage(color, text)
    if activation then
        if text:find("предметы этого типа уже используются") then
            sampSendClickTextdraw(65535)
        	route_enter = false
        	stop_flood_enter = false
            lua_thread.create(function()
            	repeat
	                sendKey(16)
            		wait(1000)
	            until stop_flood_enter or not activation
            end)
        end

        if text:find("Упакуйте ресурсы") then
        	stop_flood_enter = true
        	stop_flood_alt = false
            lua_thread.create(function()
            	wait(1000)
            	repeat
	                sendKey(1024)
            		wait(1000)
	            until stop_flood_alt or not activation
            end)
        end

        if text:find("Вы уже упаковываете ресурсы") then
        	stop_flood_alt = true
        end

        if text:find("Отнесите ресурсы на склад") then
        	stop_flood_alt = true
			local int = getMarshrutStart()
        	if int > 0 and int <= 12 then
				my_stol = int
				route_run = copy_table(route_stol_to_sda4a[int])
			end
        end

        if text:find("Проследуйте к столу для начала производства") then
        	if my_stol == -1 then
        		my_stol = findStolTo3dText()
        	end
        	if my_stol >= 1 and my_stol <= 12 then
        		route_run = copy_table(route_material_get_to_stol[my_stol])
        		route_enter = true
        	end
        end

        if text:find("Вы заняли стол") then
        	route_run = copy_table(route_to_start)
        end
    end
end

