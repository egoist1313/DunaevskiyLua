function cmd(param)
	if activation then
		activation = false
		printStringNow("bCraft - OFF", 3000)
	else
		if param:find("%d+") then
			activation = true
			delay = tonumber(param:match("(%d+)"))
			printStringNow("bCraft - ON~n~Delay: "..delay, 3000)
			if notf == nil then
				notf = 1
				--sampAddChatMessage("Активировать поиск стола: /bstol. Стоять лучше напротив третьего стола!", -1)
			end
		else
			sampAddChatMessage(" /bcraft [ms] - Введите задержку кликера в МС", -1)
		end
	end
end

function route_cmd()
	if not route_rec then
		route_arr = ""
		route_rec = true
		printStringNow("Route REC - ON~n~G - STOP", 1000)
	else
		setClipboardText(route_arr)
		route_arr = ""
		route_rec = false
		printStringNow("Route REC - OFF", 1000)
	end
end

