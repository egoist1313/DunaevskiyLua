function findStolTo3dText()
	local result = -1
	for k, v in pairs(_3dText) do
		for i = 1, #stol_all do
			local _, id = sampGetPlayerIdByCharHandle(PLAYER_PED)
			local name = sampGetPlayerNickname(id)
			local distance = getDistanceBetweenCoords3d(stol_all[i][1], stol_all[i][2], stol_all[i][3], v[1].x, v[1].y, v[1].z)
			if distance < 0.5 and v[2]:find(name) then
				result = i
				break
			end
		end
		if result ~= -1 then break end
	end
	return result
end

